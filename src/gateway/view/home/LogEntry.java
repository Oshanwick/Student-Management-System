    private void btnEnterLogMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnEnterLogMouseClicked
        try {
            String classId = ClassController.getClassId(comboclass.getSelectedItem().toString());
            String date = txtToday.getText();
            String time = txtTime.getText();
            String content = txtAreaEnter.getText();

            Log log = new Log(classId, date, time, content);

            int res = LogController.enterLog(log);
            if (res < 0) {
                JOptionPane.showMessageDialog(null, "Log Succesfully Entered");
            } else {
                JOptionPane.showMessageDialog(null, "Entry Unsuccesfull");
            }

            setComboBoxes();
            ArrayList<JTextField> fields = new ArrayList<>();
            fields.add(txtTime);
            fields.add(txtToday);
            ClearFields.clearTxtField(fields);

        } catch (SQLException | ClassNotFoundException ex) {
            Logger.getLogger(LogEntry.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_btnEnterLogMouseClicked

    private void combosubjectItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_combosubjectItemStateChanged
//        String subject=combosubject.getSelectedItem().toString();
//        String grade=combograde.getSelectedItem().toString();
        if (ifConstructorRun) {
            try {
                String teacher = TeacherController.getTeacherId(comboteacher.getSelectedItem().toString());
                String subject = SubjectController.getSubjectId(combosubject.getSelectedItem().toString());
                String grade = GradeController.getGradeId(combograde.getSelectedItem().toString());

                ArrayList<String> courses = CourseController.getFilteredCourses(teacher, subject, grade);
                for (String course : courses) {
                    combocourse.removeAll();
                    combocourse.addItem(course);
                }
            } catch (SQLException | ClassNotFoundException ex) {
                Logger.getLogger(LogEntry.class.getName()).log(Level.SEVERE, null, ex);
            }
        }

    }//GEN-LAST:event_combosubjectItemStateChanged

