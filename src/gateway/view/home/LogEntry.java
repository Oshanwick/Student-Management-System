    private void combogradeItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_combogradeItemStateChanged
        if (ifConstructorRun) {
            try {
                String teacher = TeacherController.getTeacherId(comboteacher.getSelectedItem().toString());
                String subject = SubjectController.getSubjectId(combosubject.getSelectedItem().toString());
                String grade = GradeController.getGradeId(combograde.getSelectedItem().toString());

                ArrayList<String> courses = CourseController.getFilteredCourses(teacher, subject, grade);
                for (String course : courses) {
                    combocourse.removeAll();
                    combocourse.addItem(course);
                }
            } catch (SQLException | ClassNotFoundException ex) {
                Logger.getLogger(LogEntry.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_combogradeItemStateChanged


