    private void jLabel6MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel6MouseClicked
        try {
            String id = txtId2.getText();
            String courseId = CourseController.getCourseId(comboCourse.getSelectedItem().toString());
            String month = comboMonth.getSelectedItem().toString();
            String date = CurInstance.Date_day();
            String time = CurInstance.Time_12_AMPM();
            MonthlyPayment pay = new MonthlyPayment(id, courseId, month, date, time);
            try {
                int res = MonthlyPaymentController.markPayment(pay);
                if (res > 0) {
                    JOptionPane.showMessageDialog(null, "Seccesfully Added");
                    clearfields();
                } else {
                    Object[] options = {"Allow In", "Do not Allow In"};
                    int response = JOptionPane.showOptionDialog(null, "Do you Let THis Student In?", "Not Payed For This Month", JOptionPane.YES_NO_CANCEL_OPTION, JOptionPane.WARNING_MESSAGE, null, options, options[0]);
                    if (response == 0) {
                        AttendanceController.markAttendance(attendance);
                    }
                    JOptionPane.showMessageDialog(null, "Attendance Marked");
                }

            } catch (SQLException | ClassNotFoundException ex) {
                Logger.getLogger(MarkAndIssueCard.class.getName()).log(Level.SEVERE, null, ex);

            }

        } catch (SQLException ex) {
            Logger.getLogger(MarkAndIssueCard.class.getName()).log(Level.SEVERE, null, ex);

        } catch (ClassNotFoundException ex) {
            Logger.getLogger(MarkAndIssueCard.class.getName()).log(Level.SEVERE, null, ex);
        }


    }//GEN-LAST:event_jLabel6MouseClicked

