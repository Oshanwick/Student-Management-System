    private void txtStudentIdKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtStudentIdKeyReleased
     String Id=txtStudentId.getText();
     String[] student=null;
        try {
            student = StudentController.getStudent(Id);
        } catch (SQLException ex) {
            Logger.getLogger(HomePanel.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(HomePanel.class.getName()).log(Level.SEVERE, null, ex);
        }
        if(student==null){ 
            txStudenttName.setText("");
            labelStudentPhoto.setIcon(new ImageIcon());
        }else{
            txStudenttName.setText(student[0]);
            labelStudentPhoto.setIcon(new ImageIcon(student[1]));
    
        }        
       
    }//GEN-LAST:event_txtStudentIdKeyReleased

