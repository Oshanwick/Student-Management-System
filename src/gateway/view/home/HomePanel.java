   private void loadPanel(String panel) throws SQLException, ClassNotFoundException{
      HomeEventBackground homeeventbackground =new HomeEventBackground(panel);
      homeeventbackground.setSize(componentpanel.getSize());
      componentpanel.removeAll();
      // System.out.println("helllo");
      componentpanel.add(homeeventbackground);
      componentpanel.repaint();
      componentpanel.revalidate();
   }
   private void loadPanel(String panel,String id,String name) throws SQLException, ClassNotFoundException{
      HomeEventBackground homeeventbackground =new HomeEventBackground(panel,id,name);
      homeeventbackground.setSize(componentpanel.getSize());
      componentpanel.removeAll();
      componentpanel.add(homeeventbackground);
      componentpanel.repaint();
      componentpanel.revalidate();
   }
