    private void checkForTeacher(String Id) throws SQLException, ClassNotFoundException{
        String[] teacher=TeacherController.getTeacher(Id);
        if(teacher==null){     
             txtTeacherName.setText("");
            labelTeacherPhoto.setIcon(new ImageIcon());
        }else{
            txtTeacherName.setText(teacher[0]);
            labelTeacherPhoto.setIcon(new ImageIcon(teacher[1]));
    
        }        
    }
   

