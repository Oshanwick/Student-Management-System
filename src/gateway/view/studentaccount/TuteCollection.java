    private void comboCourses() {
        try {
            ArrayList<String> courses = CourseController.getHisCourses(addmissionNumber);
            for (String course : courses) {
                comboCourses.addItem(course);
            }
            isConstructorRun = true;
        } catch (SQLException ex) {
            Logger.getLogger(TuteCollection.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(TuteCollection.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
