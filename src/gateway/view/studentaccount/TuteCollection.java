    private void comboCoursesItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_comboCoursesItemStateChanged
        if (isConstructorRun) {
            try {
                tableTutes.removeAll();
                ArrayList<String[]> myTutes = RecievedTuteController.getmyTuteDetails(addmissionNumber, CourseController.getCourseId(comboCourses.getSelectedItem().toString()));
                for (String[] myTute : myTutes) {
                    dtm.addRow(myTute);
                }
            } catch (SQLException ex) {
                Logger.getLogger(TuteCollection.class.getName()).log(Level.SEVERE, null, ex);
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(TuteCollection.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_comboCoursesItemStateChanged

