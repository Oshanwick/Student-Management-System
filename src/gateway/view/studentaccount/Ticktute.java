    private void jButton2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton2MouseClicked
        try {
            ArrayList<RecievedTute> tutes = new ArrayList<>();
            int CountTutes = dlmAdd.getSize();
            for (int i = 0; i < dlmAdd.getSize(); i++) {
                Object tute = dlmAdd.getElementAt(i);
                RecievedTute recievedTute = new RecievedTute(admissionNumber, TuteController.getTuteId(tute.toString()), CurInstance.Date());
                dlmcollectedTutes.addElement(tute);
                tutes.add(recievedTute);
            }
            dlmAdd.removeAllElements();
            int count = RecievedTuteController.addReceivedTute(tutes);
            if (count == CountTutes) {
                System.out.println("All Tutes Added Succesfully ");
            }
        } catch (SQLException ex) {
            Logger.getLogger(Ticktute.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(Ticktute.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton2MouseClicked


