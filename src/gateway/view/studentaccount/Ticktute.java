    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        try {
            //Arraylist 2k tyenawwa gennaganna --- ekak thamai my tutes , anika thamai no yet receive ewa

            ArrayList<String> myTutes = RecievedTuteController.getMyTutes(admissionNumber, CourseController.getCourseId(comboCourse.getSelectedItem().toString()));
            ArrayList<String> uncollectedTuutes = TuteController.getUncollectedTutes(myTutes, CourseController.getCourseId(comboCourse.getSelectedItem().toString()));
            for (String myTute : myTutes) {
                dlmcollectedTutes.addElement(myTute);
            }
            for (String uncollectedTute : uncollectedTuutes) {
                dlmUncollectedTutes.addElement(uncollectedTute);
            }
        } catch (SQLException ex) {
            Logger.getLogger(Ticktute.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(Ticktute.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_jButton1MouseClicked

