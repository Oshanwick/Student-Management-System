    private void jButton3MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton3MouseClicked
        try {
            ArrayList<Tute> tutes = new ArrayList<>();
            for (int i = 0; i < dtmNew.getRowCount(); i++) {
                Tute tute = new Tute(dtmNew.getValueAt(i, 1).toString(), CourseController.getCourseId(dtmNew.getValueAt(i, 0).toString()), dtmNew.getValueAt(i, 2).toString()
                        , CurInstance.Date());
                tutes.add(tute);
            }
            int count=TuteController.addTute(tutes);
            if(count==dtmNew.getRowCount()){
                JOptionPane.showMessageDialog(null, "Tutes Succesfully Added");
            }else{
                JOptionPane.showMessageDialog(null, "Not Added");
            }
        } catch (SQLException ex) {
            Logger.getLogger(AddTute.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(AddTute.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton3MouseClicked


