    private void comboTeacher() {
        try {
            ArrayList<String[]> teachers = TeacherController.getTeacherName();
            for (String[] teacher : teachers) {
                comboTeacher.addItem(teacher[0]);
                comboTeacherDown.addItem(teacher[0]);
            }
        } catch (SQLException | ClassNotFoundException ex) {
            Logger.getLogger(AddTute.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void comboCourse() {
        try {
            ArrayList<String[]> Courses = CourseController.getCourse();
            for (String[] course : Courses) {
                comboCourse.addItem(course[0]);
                comboCourseDown.addItem(course[0]);
            }
        } catch (SQLException | ClassNotFoundException ex) {
            Logger.getLogger(AddTute.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

