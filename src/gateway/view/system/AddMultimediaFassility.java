    private void btnSaveMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnSaveMouseClicked
         
        ArrayList<MultimediaFascility> rows=new ArrayList<>();
        for(int i=0;i<dtm.getRowCount();i++){
            String id=tablemultimedia.getValueAt(i,0).toString();
            String fascility=tablemultimedia.getValueAt(i,1).toString();
           MultimediaFascility mediafascility= new MultimediaFascility(id,fascility);
           rows.add(mediafascility);
        }
        try {
            MultimediaFascilityController.addFascility(rows);
        } catch (SQLException | ClassNotFoundException ex) {
            Logger.getLogger(AddMultimediaFassility.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnSaveMouseClicked

