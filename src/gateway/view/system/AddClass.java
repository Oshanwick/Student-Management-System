    private void jPanel18MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jPanel18MouseClicked
        int crowd = Integer.parseInt(txtCrowd.getText());
        String furniture = comboFurniture.getSelectedItem().toString();
        String multimedia = comboMultimedia.getSelectedItem().toString();
        String AC;
        if (radioBtnAC.isSelected()) {
            AC = "AC";
        } else {
            AC = "Non-AC";
        }
        try {
            ArrayList<String> places = PlaceController.filterPlace(crowd, furniture, multimedia, AC);
            for (String place : places) {
                dlm.addElement(place);
            }
        } catch (SQLException | ClassNotFoundException ex) {
            Logger.getLogger(AddClass.class.getName()).log(Level.SEVERE, null, ex);
        }


    }//GEN-LAST:event_jPanel18MouseClicked

