    private void loadComboBoxes() throws SQLException, ClassNotFoundException {
        teacherID();
        teacherName();
        subject();
        grade();
        furniture();
        multimedia();
    }

    private void teacherID() throws SQLException, ClassNotFoundException {
//        ArrayList<String[]> teachers= TeacherController.getTeacherID();
        for (String[] teacher : teachers) {
            comboTeacherId.addItem(teacher[0]);
        }
    }

    private void teacherName() throws SQLException, ClassNotFoundException {
//        ArrayList<String[]> teachers= TeacherController.getTeacherName();
        for (String[] teacher : teachers) {
            comboTeacherName.addItem(teacher[1]);
        }
    }

    private void subject() throws SQLException, ClassNotFoundException {
        ArrayList<String[]> subjects = SubjectController.getSubject();
        for (String[] subject : subjects) {
            comboSubject.addItem(subject);
        }
    }

    private void grade() throws SQLException, ClassNotFoundException {
        ArrayList<String[]> grades = GradeController.getGrade();
        for (String[] grade : grades) {
            comboGrade.addItem(grade);
        }
    }

    private void furniture() throws SQLException, ClassNotFoundException {
        ArrayList<String[]> furnitures = FurnitureController.getFurniture();
        for (String[] furniture : furnitures) {
            comboFurniture.addItem(furniture);
        }
    }

    private void multimedia() throws SQLException, ClassNotFoundException {
        ArrayList<String[]> medias = MultimediaController.getMultimedia();
        for (String[] media : medias) {
            comboTeacherId.addItem(media);
        }
    }

    private void filterCourse() throws SQLException, ClassNotFoundException {
        String teacherID = comboTeacherId.getSelectedItem().toString();
        String gradeID = GradeController.getGradeId(comboGrade.getSelectedItem().toString());
        String subjectID = SubjectController.getSubjectId(comboSubject.getSelectedItem().toString());
        ArrayList<String[]> courses = CourseController.viewFilteredCourse(gradeID, subjectID, teacherID);
    }

