    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        try {
            dtm.setRowCount(0);
            SimpleDateFormat sdf=new SimpleDateFormat("YYYY-MM-dd");
            String Date =sdf.format(txtDate.getDate());
            ArrayList<Object[]> rowDatas=MonthlyPaymentController.showTodayPaymentsOfAllTeachers(Date);
            for(Object[] row:rowDatas){
                dtm.addRow(row);
            }
        } catch (SQLException ex) {
            Logger.getLogger(TodaysTeacherPayments.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(TodaysTeacherPayments.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1MouseClicked

    private void jLabel2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel2MouseClicked
        try {      
            JRTableModelDataSource dataSource=new JRTableModelDataSource(dtm);
            
            
            JasperReport jr=JasperCompileManager.compileReport(getClass().getResourceAsStream("TodayTeacherPaymentReport.jrxml"));// to compile tghis report.
            JasperPrint jp=JasperFillManager.fillReport(jr,null,dataSource);
            JasperViewer.viewReport(jp);
        } catch (JRException ex) {
            Logger.getLogger(TodaysTeacherPayments.class.getName()).log(Level.SEVERE, null, ex);
        }
       
    }//GEN-LAST:event_jLabel2MouseClicked


